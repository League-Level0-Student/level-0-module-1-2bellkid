
public class myAges {
public static void main(String[] args) {
	for (int i = 2006; i <= 2017; i++ ) {

	if(i-2006==1) {
		System.out.println(" In "+i+ " I was " +(i-2006)+ " year old");
		}
	else if(i==2017) {
		System.out.println(" In "+i+ " I am " +(i-2006)+ " years old");
	}
	else {
		System.out.println(" In "+i+ " I was " +(i-2006)+ " years old");
	}
}
}
}


